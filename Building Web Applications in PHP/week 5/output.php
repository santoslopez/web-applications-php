<?php
    $x = "15" + 27;


    /*
    echo is a language construct can be threated like a function
    with one parameter. without parenthesis, it multiple paremeters
    */
    echo $x;

    echo("\n");

    echo $x, "\n";


    /*
        print is a function only one parameter but parentheses are
        optional so it can look like a language constructor
    */
    print $x;
    print "\n";

    print($x);

    print("\n");
?>